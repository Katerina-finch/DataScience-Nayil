"""Демонстрация правил вероятности на примерах."""

# %%
print()
print()
print("------------------------------------------------")
print(" Демонстрация правил вероятности на примерах")
print("------------------------------------------------")
print()
print()
print("Пример 1: Несовместные события (орел и решка)")
print()
print()
p_head: float = 0.5  # Вероятность орла
p_tail: float = 0.5  # Вероятность решки

# %%
# Сложение вероятностей для несовместных событий
total_sum: float = p_head + p_tail
print(f"P(орел) + Р(решка) = {p_head} + {p_tail} = {total_sum}")
print("Сумма = 1 (полная вероятность)")
print()

# %%
# Умножение несовместных событий
impossible: float = p_head * p_tail
print(f"P(орел) x Р(решка) = {p_head} x {p_tail} = {impossible}")
print("Произведение = 0 (невозможное событие)")
print()
print(
    (
        "Сумма вероятностей всех возможных исходов эксперимента всегда должна "
        "быть = 1. Если сумма вероятностей равна 0.25, это не"
        "соответствует полной "
        "группе событий и означает, что учтены не все возможные исходы "
        "эксперимента."
    ),
)

# %%
print(
    (
        "Значение 0.25, скорее всего, является вероятностью отдельного "
        "сочетания двух независимых событий (например, выпадение"
        "орла и четного "
        "числа). Однако если рассматривать полную группу событий, то сумма "
        "вероятностей всех возможных сочетаний должна составить 1."
    ),
)

# %%
print()
print()
print("Пример 2: Независимые события (орел и четное на кубике)")
print()
print()
p_even: float = 0.5  # Вероятность четного числа

# %%
# Умножение вероятностей для независимых событий
total_mult: float = p_head * p_even
print(f"P(орел) x Р(четное) = {p_head} x {p_even} = {total_mult}")
print("Произведение = 0.25 (вероятность обоих событий)")
print()

