print("Пример 1: Несовместные события (орел и решка)")
print()
print()
p_head: float = 0.5 # Вероятность орла
p_tail: float = 0.5 # Вероятность решки

# Сложение вероятностей для несовместных событий 
total_sum: float = p_head + p_tail
print(f"P(орел) + Р(решка) = {p_head} + {p_tail} = {total_sum}")
print("Сумма = 1 (полная вероятность)")
print()

# Умножение несовместных событий
impossible: float = p_head * p_tail
print(f"P(орел) x Р(решка) = {p_head} x {p_tail} = {impossible}")
print("Произведение = 0 (невозможное событие)")
print()
